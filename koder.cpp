packet::packet(uint16_t start, uint16_t type, uint16_t len, const char* raw_data) {
	auto& h = data_packet.header;
	
	h.start = start;
	h.type = type;
	h.len = len + packet_defs::header_len;

	if (len > packet_defs::max_payload_len) {
		throw std::exception("bad length of a packet");
	}
	else {
		data_packet.data = new uint16_t[packet_defs::header_len + len];
		data_packet.data[0] = h.start;
		data_packet.data[1] = h.type;
		data_packet.data[2] = h.len;

		uint16_t crc = 0;

		for (uint16_t i = packet_defs::header_len; i < h.len; ++i) {
			data_packet.data[i] = (uint16_t)raw_data[i - packet_defs::header_len];
			crc += (uint16_t)raw_data[i - packet_defs::header_len];
		}

		crc = crc % packet_defs::crc_mod;
		h.crc = crc;
		data_packet.data[3] = crc;
	}
}

packet::~packet() {
	delete[] this->data_packet.data;
	this->data_packet.data = nullptr;
}

packet_defs::data_packet_t& packet::get_packet(){ 
	return data_packet; 
}

coder::coder(uint8_t coder_flags) {
	this->coder_flags = coder_flags;
}

constexpr uint8_t xor_const = 0x69;
constexpr uint8_t xor_stream_len = 10;
constexpr uint8_t xor_stream[10]{ 1145 , 1352 , 2148 , 916 , 1244 , 2267 , 82 , 1301 , 1084 , 314 };

const packet_defs::data_packet_t& coder::get_coded_packet(packet_defs::data_packet_t& p) const {
	if (coder_flags & coder_defs::homofon_cipher) {
		for (uint16_t i = 0; i < p.header.len; ++i) {
			p.data[i] = homofony[p.data[i]][rand() % 10];
		}
	}
	else {
		//empty else
	}
	
	if (coder_flags & coder_defs::xor_const_cipher) {
		for (uint16_t i = 0; i < p.header.len; ++i) {
			p.data[i] = p.data[i] ^ xor_const;
		}
	}
	else if (coder_flags & coder_defs::xor_stream_cipher) {
		uint8_t i = 0;

		for (uint16_t i = 0; i < p.header.len; ++i) {
			p.data[i] = p.data[i] ^ xor_stream[i++];
			if (i == 10) {
				i = 0;
			}
		}
	}
	return p;
}